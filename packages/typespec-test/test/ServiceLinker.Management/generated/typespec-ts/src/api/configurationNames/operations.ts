import { ServiceLinkerManagementContext as Client } from "../index.js";
import {
  errorResponseDeserializer,
  _ConfigurationNameResult,
  _configurationNameResultDeserializer,
  ConfigurationNameItem,
} from "../../models/models.js";
import {
  PagedAsyncIterableIterator,
  buildPagedAsyncIterator,
} from "../../static-helpers/pagingHelpers.js";
import { expandUrlTemplate } from "../../static-helpers/urlTemplate.js";
import { ConfigurationNamesListOptionalParams } from "./options.js";
import {
  StreamableMethod,
  PathUncheckedResponse,
  createRestError,
  operationOptionsToRequestParameters,
} from "@azure-rest/core-client";

export function _listSend(
  context: Client,
  options: ConfigurationNamesListOptionalParams = { requestOptions: {} },
): StreamableMethod {
  const path = expandUrlTemplate(
    "/providers/Microsoft.ServiceLinker/configurationNames{?api%2Dversion,%24filter,%24skipToken}",
    {
      "api%2Dversion": context.apiVersion,
      "%24filter": options?.filter,
      "%24skipToken": options?.skipToken,
    },
    {
      allowReserved: options?.requestOptions?.skipUrlEncoding,
    },
  );
  return context
    .path(path)
    .get({
      ...operationOptionsToRequestParameters(options),
      headers: {
        accept: "application/json",
        ...options.requestOptions?.headers,
      },
    });
}

export async function _listDeserialize(
  result: PathUncheckedResponse,
): Promise<_ConfigurationNameResult> {
  const expectedStatuses = ["200"];
  if (!expectedStatuses.includes(result.status)) {
    const error = createRestError(result);
    error.details = errorResponseDeserializer(result.body);
    throw error;
  }

  return _configurationNameResultDeserializer(result.body);
}

/** Lists the configuration names generated by Service Connector for all target, client types, auth types. */
export function list(
  context: Client,
  options: ConfigurationNamesListOptionalParams = { requestOptions: {} },
): PagedAsyncIterableIterator<ConfigurationNameItem> {
  return buildPagedAsyncIterator(
    context,
    () => _listSend(context, options),
    _listDeserialize,
    ["200"],
    { itemName: "value", nextLinkName: "nextLink" },
  );
}
